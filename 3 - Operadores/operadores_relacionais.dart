void main() {
  // Operadores Relacionais
  // == (igualdade)
  // != (diferença)
  // >  (maior)
  // <  (menor)
  // <= (menor igual)
  // >= (maior igual)

  int idade = 30;
  String nome = 'Marcio';

  if (nome == 'Marcio') {
    print('Nome é Marcio');
  }

  if (idade == 30) {
    print('idade é 30');
  }

  if (idade != 29) {
    print('idade é 30 e é diferente de 29');
  }

  if (idade > 20) {
    print('idade é maior que 20');
  }

  if (idade < 40) {
    print('idade é menor que 40');
  }

  if (idade >= 30) {
    print('idade é maior ou igual a 30');
  }

  if (idade <= 30) {
    print('idade é menor ou igual a 30');
  }
}
